//function that takes an array of words and returns the longest word. Use the reduce() array method
let lesmots=["Ordinateur","clavier","souris","imprimente","multiplexeurs","extincteurs"]
const resultat=lesmots.reduce((item,longueur)=> {
    item.length[longueur]=(item.length[longueur]);
    return longueur;
})
console.log("the longest word in array is ",resultat);
